/*
逻辑：若 sum() 仅传入了第一个参数，则设置第二个形参 b 的值为默认值 1；第三个形参 c 的值为默认值 2。
*/

// ES6 之前的做法
function sum(a, b, c) {
  b = b === undefined && 1; // 不要写成 b = b || 1; 这么写的话 b 如果传的是 0 那么 b 也会取默认值 1
  c = c === undefined && 2;
  return a + b + c;
}

sum(10); // => 13
sum(11); // => 14
sum(12); // => 15

// 使用 ES6 中的默认参数来实现
function sum(a, b = 1, c = 2) {
  return a + b + c;
}

sum(10); // => 13
sum(11); // => 14
sum(12); // => 15

// 若想要让第二个参数取默认值，第三个参数为我们传递的值，只要想下面这样来调用即可。
sum(1, undefined, 8); // => 10